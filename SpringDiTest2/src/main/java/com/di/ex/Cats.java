package com.di.ex;

public class Cats {
	public void catsName(String firstCatName, String secondCatName) {
		System.out.println("첫 번째 고양이 이름은 "+firstCatName);
		System.out.println("두 번째 고양이 이름은 "+secondCatName);
	}
	
	public void catsAge(int firstCatAge, int secondCatAge) {
		System.out.println("첫 번째 고양이 나이는 "+firstCatAge);
		System.out.println("두 번째 고양이 나이는"+secondCatAge);
	}
}
